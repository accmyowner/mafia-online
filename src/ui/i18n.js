export const LANGUAGES = [
  { id: 'ru', label: 'Русский' },
  { id: 'en', label: 'English' },
];

/** Текущий язык из настроек клиента. */
export function lang() {
  return DICT[store.get().prefs.lang] ? store.get().prefs.lang : 'ru';
}

/** Перевод с подстановкой {переменных}. */
export function t(key, vars = {}) {
  const table = DICT[lang()] || DICT.ru;
  let text = table[key] ?? DICT.ru[key] ?? key;
  for (const [name, value] of Object.entries(vars)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}

/** Локализованное поле объекта вида {ru: '...', en: '...'}. */
export function tr(field) {
  if (!field) return '';
  return typeof field === 'string' ? field : field[lang()] ?? field.ru;
}

export function setLanguage(code) {
  store.patchPrefs({ lang: DICT[code] ? code : 'ru' });
  document.documentElement.lang = lang();
  bus.emit('lang:change', lang());
}
